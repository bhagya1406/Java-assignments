class Demo{
	public static void main(String[]args){
	char ch1='a';
	char ch2='b';
	int a=97;
	int b=117;
		if(a>=97 && b>=117){
			System.out.println(ch1 + " is vowel");
			System.out.println(ch2 + " is consonant");
			a++;
			b++;
		}else{
			System.out.println();
		}
	}
}

